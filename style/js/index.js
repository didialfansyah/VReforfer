function formBlur(gId){
    let lbl = document.getElementById("l" + gId);
    lbl.setAttribute("class", "animate__animated animate__fadeOutDown");

    let thisId = document.getElementById(gId);
    let note = document.getElementById("note" + gId)
    if(thisId.value === ""){
        thisId.setAttribute("class", "form-control is-invalid");
        note.innerHTML = "Form Cannot Be Empty !";
        note.setAttribute("class", "box feedRed animate__animated animate__fadeInRight");
        thisId.setAttribute("stt", "0");
    }else{
        thisId.setAttribute("class", "form-control is-valid");
        note.setAttribute("class", "box animate__animated animate__fadeOutRight"); 
        thisId.setAttribute("stt", "1");
    }

    if(gId === "email"){
        let regEmail = /^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/
        if(regEmail.test(thisId.value) === false){
            thisId.setAttribute("class", "form-control is-invalid");
            note.innerHTML = "Valid email is required: ex@abc.yz";
            note.setAttribute("class", "box feedRed animate__animated animate__fadeInRight");
            thisId.setAttribute("stt", "0");
        }else{
            thisId.setAttribute("class", "form-control is-valid");
            note.setAttribute("class", "box animate__animated animate__fadeOutRight");
            thisId.setAttribute("stt", "1"); 
        }
    }
}

function formValid(){
    let name = document.getElementById("name");
    let email = document.getElementById("email");
    let subject = document.getElementById("subject");
    let message = document.getElementById("message");
    
    if(name.getAttribute("stt") == 0){
        name.focus();
    }else if(email.getAttribute("stt") == 0){
        email.focus();
    }else if(subject.getAttribute("stt") == 0){
        subject.focus();
    }else if(message.getAttribute("stt") == 0){
        message.focus();
    }else{
        let Alrt = document.getElementById("alSuccess");
        Alrt.setAttribute("class", "alert alert-success animate__animated animate__fadeInDown");
        name.value = "";
        name.setAttribute("class", "form-control");
        name.setAttribute("stt", "0");

        email.value = "";
        email.setAttribute("class", "form-control");
        email.setAttribute("stt", "0");
        
        subject.value = "";
        subject.setAttribute("class", "form-control");
        subject.setAttribute("stt", "0");

        message.value = "";
        message.setAttribute("class", "form-control");
        message.setAttribute("stt", "0");
    }
}

